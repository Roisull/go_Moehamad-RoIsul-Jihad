package main

import "fmt"

func main() {
	// tipe data boolean
	var isLogin bool = false
	fmt.Println(isLogin)

	// tipe data int
	var prime int = 7
	fmt.Println(prime)

	// tipe data float
	var decimal float64 = 45.6
	fmt.Println(decimal)

	// tipe data string 
	var hello string = "Hello World"
	fmt.Println(hello)

	// tipe data const / tetap
	const pi = 3.14
	fmt.Println(pi)
}